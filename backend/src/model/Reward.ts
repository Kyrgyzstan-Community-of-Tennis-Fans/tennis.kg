import mongoose from 'mongoose';

const Schema = mongoose.Schema;

const RewardSchema = new Schema(
  {
    user: {
      type: Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    title: {
      type: String,
      required: true,
      unique: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const Reward = mongoose.model('Reward', RewardSchema);
